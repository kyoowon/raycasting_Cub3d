/*
** factor - c : char.
** return - 0 or 1.
** function - it is Checking an alphabet.
*/

#include "libft.h"

int		ft_isalpha(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}
